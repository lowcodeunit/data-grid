
/**
 * Model for handling no data in the grid
 */
export class DataGridNoDataModel {

    /**
     * Dynamic component to pass in
     */
    public Component?: any;

    /**
     * No data message
     */
    public Info?: string;

    /**
     * Display message inline, using content projection
     */
    public ShowInline?: boolean;

    /**
     * Message title
     */
    public Title?: string;

}